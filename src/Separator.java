public class Separator {
    public void drawSeparation(String message) {
        System.out.println("                              ");
        System.out.println("==============================");
        System.out.println("                              ");
        System.out.println(message);
        System.out.println("                              ");
        System.out.println("==============================");
        System.out.println("                              ");
    }
}